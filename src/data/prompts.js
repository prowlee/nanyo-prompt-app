const getSearchUrl = (title) => {
  return `https://www.google.com/search?q=${encodeURIComponent("南陽市 プロンプト " + title)}`;
};

const decodeUrl = (linkId) => {
  if (!linkId || linkId === 0 || linkId === "0") return null;
  const s = String(linkId);
  if (s.startsWith("G")) {
    const num = s.slice(1);
    return `https://nanyo-line.github.io/prompt/${num}.html`;
  }
  if (s === "573b") {
    return "https://nanyo-city.jpn.org/download_prompt/download.php?download=573b";
  }
  return `https://nanyo-city.jpn.org/download_prompt/download.php?download=${s}`;
};

